// =====================================================================
// PE 调试台保存的 override 自动注入（key: capsule_pe_overrides_v1）
// 这样在 PE 页改了 prompt 并点「保存」后，主页 index.html 也会用新的版本
// =====================================================================
(function applyPEOverrides() {
  try {
    const raw = localStorage.getItem('capsule_pe_overrides_v1');
    if (!raw) return;
    const ov = JSON.parse(raw);
    if (ov.system) window.CAPSULE_PROMPTS.system = ov.system;
    if (ov.modes) {
      Object.entries(ov.modes).forEach(([k, v]) => {
        if (window.CAPSULE_PROMPTS.modes[k]) {
          if (v.prompt) window.CAPSULE_PROMPTS.modes[k].prompt = v.prompt;
          if (v.examples) window.CAPSULE_PROMPTS.modes[k].examples = v.examples;
        }
      });
    }
  } catch (_) { /* 静默 */ }
})();
